// take a array of arr [10,20,30,40,98,3,51]
// max and min find 
// max+min
// max-min


const arr = [10, 20, 30, 40, 98, 3, 51];

// Find maximum and minimum values
const max = Math.max(...arr);
const min = Math.min(...arr);

// Display results
console.log("Max:", max);
console.log("Min:", min);

// Max + Min
console.log("Max + Min:", max + min);

// Max - Min
console.log("Max - Min:", max - min);



